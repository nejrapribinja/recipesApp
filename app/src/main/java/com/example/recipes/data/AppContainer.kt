package com.example.recipes.data

import com.example.recipes.network.RecipesApiService
import com.jakewharton.retrofit2.converter.kotlinx.serialization.asConverterFactory
import kotlinx.serialization.json.Json
import okhttp3.MediaType.Companion.toMediaType
import retrofit2.Retrofit

interface AppContainer {
    val recipesRepository: RecipesRepository
}

class DefaultAppContainer : AppContainer {
    private val BASE_URL = "https://www.themealdb.com/api/json/v1/1/"

    private val retrofit: Retrofit = Retrofit.Builder()
        .addConverterFactory(Json.asConverterFactory("application/json".toMediaType()))
        .baseUrl(BASE_URL)
        .build()

    private val retrofitService: RecipesApiService by lazy {
        retrofit.create(RecipesApiService::class.java)
    }

    override val recipesRepository: RecipesRepository by lazy {
        DefaultRecipesRepository(retrofitService)
    }
}
